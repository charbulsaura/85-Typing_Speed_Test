count = 30
words = 0
characters = 0
WPM = 0
CPM = 0

def change_global_variable():
    global count, words, characters, WPM, CPM
    count = 1
    words = 1
    characters = 1
    WPM = 1
    CPM = 1

print(count)
print(words)
print(characters)
print(WPM)
print(CPM)
change_global_variable()
print(count)
print(words)
print(characters)
print(WPM)
print(CPM)
